package com.company;

public class Cirle {
    private Double radius;

    public Cirle() {
        super();
    }

    public Cirle(Double radius) {
        super();
        this.radius = radius;
    }

    public Double getRadius() {
        return radius;
    }

    public void setRadius(double radius) {
        this.radius = radius;
    }
}
